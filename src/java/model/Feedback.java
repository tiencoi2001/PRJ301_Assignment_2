package model;

/**
 *
 * @author Vu Duc Tien
 */
public class Feedback {
    private int id;
    private String email;
    private String phone;
    private String detail;

    public Feedback() {
    }

    public Feedback(int id, String email, String phone, String detail) {
        this.id = id;
        this.email = email;
        this.phone = phone;
        this.detail = detail;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getDetail() {
        return detail;
    }

    public void setDetail(String detail) {
        this.detail = detail;
    }
}
